import sqlite3
import os
from datetime import datetime

from werkzeug.security import generate_password_hash

DB_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "wound_database.db")

def get_db_connection():
    """
    Establishes and returns an SQLite database connection.
    Enables dictionary row factory for column-name keyed indexing.
    """
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn

def init_db():
    """
    Initializes the SQLite tables (users, reports) if they do not already exist.
    Inserts a default admin account if not already present.
    """
    # Ensure the parent directory exists
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    
    conn = get_db_connection()
    cursor = conn.cursor()
    
    # 1. Create Users Table
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS users (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            username TEXT UNIQUE NOT NULL,
            email TEXT NOT NULL,
            password_hash TEXT NOT NULL,
            role TEXT DEFAULT 'user',
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    """)
    
    # 2. Create Reports Table
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS reports (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id INTEGER NOT NULL,
            filename TEXT NOT NULL,
            wound_type TEXT NOT NULL,
            severity TEXT NOT NULL,
            major_minor TEXT NOT NULL,
            infection_possibility TEXT NOT NULL,
            emergency_level TEXT NOT NULL,
            confidence REAL NOT NULL,
            first_aid TEXT NOT NULL,
            red_pixel_count INTEGER DEFAULT 0,
            redness_ratio REAL DEFAULT 0.0,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
        )
    """)
    
    # Check if a default admin exists; if not, create a seed one
    # Note: Flask app will handle registration, but we initialize a seed user/admin shell if empty.
    cursor.execute("SELECT COUNT(*) FROM users WHERE role = 'admin'")
    if cursor.fetchone()[0] == 0:
        # Default admin credentials: username 'admin', email 'admin@woundai.com'
        # Password hash is generated by Werkzeug security for 'admin123'
        admin_hash = generate_password_hash("admin123")
        try:
            cursor.execute("""
                INSERT INTO users (username, email, password_hash, role)
                VALUES ('admin', 'admin@woundai.com', ?, 'admin')
            """, (admin_hash,))
            print("[Database] Seeded default admin account (username: admin, password: admin123)")
        except sqlite3.IntegrityError:
            pass

    conn.commit()
    conn.close()
    print("[Database] SQLite Tables verified and initialized.")

