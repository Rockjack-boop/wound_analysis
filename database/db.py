def get_dashboard_stats():
    """Compiles key statistical aggregates for user dashboards."""
    conn = get_db_connection()
    cursor = conn.cursor()
    
    stats = {}
    
    # Total patients / reports
    cursor.execute("SELECT COUNT(*) FROM users")
    stats["total_users"] = cursor.fetchone()[0]
    
    cursor.execute("SELECT COUNT(*) FROM reports")
    stats["total_reports"] = cursor.fetchone()[0]
    
    # Severity split
    cursor.execute("SELECT COUNT(*) FROM reports WHERE major_minor = 'Major'")
    stats["major_count"] = cursor.fetchone()[0]
    
    cursor.execute("SELECT COUNT(*) FROM reports WHERE major_minor = 'Minor'")
    stats["minor_count"] = cursor.fetchone()[0]
    
    # Infection status
    cursor.execute("SELECT COUNT(*) FROM reports WHERE infection_possibility = 'High'")
    stats["high_infection_count"] = cursor.fetchone()[0]
    
    # Average confidence
    cursor.execute("SELECT AVG(confidence) FROM reports")
    avg_conf = cursor.fetchone()[0]
    stats["avg_confidence"] = round(avg_conf, 1) if avg_conf else 0.0
    
    # Wound type counts
    cursor.execute("SELECT wound_type, COUNT(*) as cnt FROM reports GROUP BY wound_type")
    wound_types = cursor.fetchall()
    stats["wound_distribution"] = {row["wound_type"]: row["cnt"] for row in wound_types}
    
    # Emergency levels count
    cursor.execute("SELECT emergency_level, COUNT(*) as cnt FROM reports GROUP BY emergency_level")
    em_levels = cursor.fetchall()
    stats["emergency_distribution"] = {row["emergency_level"]: row["cnt"] for row in em_levels}
    
    conn.close()
    return stats
