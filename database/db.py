def register_user(username, email, password_hash, role='user'):
    """Inserts a new user record. Returns True on success, False if username taken."""
    conn = get_db_connection()
    cursor = conn.cursor()
    success = False
    try:
        cursor.execute("""
            INSERT INTO users (username, email, password_hash, role)
            VALUES (?, ?, ?, ?)
        """, (username, email, password_hash, role))
        conn.commit()
        success = True
    except sqlite3.IntegrityError:
        success = False
    finally:
        conn.close()
    return success

def get_user_by_username(username):
    """Retrieves a user row dictionary matching the specified username."""
    conn = get_db_connection()
