def add_report(user_id, filename, wound_type, severity, major_minor, 
               infection_possibility, emergency_level, confidence, first_aid,
               red_pixel_count=0, redness_ratio=0.0):
    """Saves a wound detection report record to the database."""
    conn = get_db_connection()
    cursor = conn.cursor()
    cursor.execute("""
        INSERT INTO reports (
            user_id, filename, wound_type, severity, major_minor,
            infection_possibility, emergency_level, confidence, first_aid,
            red_pixel_count, redness_ratio
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
    """, (user_id, filename, wound_type, severity, major_minor,
          infection_possibility, emergency_level, confidence, first_aid,
          red_pixel_count, redness_ratio))
    report_id = cursor.lastrowid
    conn.commit()
    conn.close()
    return report_id

def get_reports_by_user_id(user_id):
    """Fetches diagnostic history records for a specific patient."""
    conn = get_db_connection()
    cursor = conn.cursor()
    cursor.execute("""
        SELECT * FROM reports 
        WHERE user_id = ? 
        ORDER BY created_at DESC
    """, (user_id,))
    rows = cursor.fetchall()
    conn.close()
    return rows

def get_all_reports():
    """Fetches all reports in the database, with joined username, for administrators."""
    conn = get_db_connection()
    cursor = conn.cursor()
    cursor.execute("""
        SELECT reports.*, users.username 
        FROM reports
        JOIN users ON reports.user_id = users.id
        ORDER BY reports.created_at DESC
    """)
    rows = cursor.fetchall()
    conn.close()
    return rows

def get_report_by_id(report_id):
    """Retrieves a single report details by its ID."""
    conn = get_db_connection()
